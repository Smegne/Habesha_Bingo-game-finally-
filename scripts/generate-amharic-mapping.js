const amharicLetters = {
  'B': 'рЅб',
  'I': 'ріарІГ',
  'N': 'ріцріЋ',
  'G': 'рїѓ',
  'O': 'рід'
};

const mapping = [];

for (let num = 1; num <= 75; num++) {
  let letter;
  if (num <= 15) letter = 'B';
  else if (num <= 30) letter = 'I';
  else if (num <= 45) letter = 'N';
  else if (num <= 60) letter = 'G';
  else letter = 'O';

  mapping.push({
    number: num,
    letter,
    filename: `${num}.mp3`,
    amharic: `${amharicLetters[letter]} ${amharicNumbers[num]}`,
    english: `${letter}-${num}`
  });
}

// Save mapping file
fs.writeFileSync(
  path.join(process.cwd(), 'public/audio/amharic/male/mapping.json'),
  JSON.stringify(mapping, null, 2)
);

